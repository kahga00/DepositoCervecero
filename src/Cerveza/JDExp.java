      public Integer consulExp(){
          Metodos_sql metodos = new Metodos_sql();
        sentence = "SELECT MAX(idexpendio) FROM  expendio";
        try {
            resc = metodos.consultarAlgo(sentence);
            while (resc.next()) {
                ultimo = resc.getInt("MAX(idexpendio)");
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDExp.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Existen " + ultimo + " expendios");
        return ultimo;
      
      }
