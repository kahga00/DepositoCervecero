    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       
       
       int e = metodos.actuCerv(cer.getIdcerveza(),cer.getIdmarca(), cer.getNombre(), 
               cer.getPresentacion(), cer.getGraduacion(), cer.getTipo(), cer.getAspecto(), cer.getProcedimientos());
       if (e > 0) {
            JOptionPane.showMessageDialog(this, "Se actualizo cerveza correctamente");
        } else {
            
            int i= metodos.guardarCer(cer.getIdcerveza(),cer.getIdmarca(),jLabel11.getText(), jLabel12.getText(), 
                jLabel13.getText(), jLabel14.getText(),
                jLabel15.getText(), jLabel16.getText());
        if (i > 0) {
            JOptionPane.showMessageDialog(this, "Se ha creado una Marca correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo guardar los datos");
        }
       }
        
        JFInicio ventana = new JFInicio();
        ventana.setVisible(true);
        this.dispose();
        super.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
    
