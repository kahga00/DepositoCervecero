    public Integer consulCer() {
        // ConexionBD Objmsql = new ConexionBD();
            Metodos_sql metodos = new Metodos_sql();
        sentence = "SELECT MAX(idcerveza) FROM  cerveza";
        try {
            resc = metodos.consultarAlgo(sentence);
            while (resc.next()) {
                ultimo = resc.getInt("MAX(idcerveza)");
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDExp.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Existen " + ultimo + " cervezas");
        return ultimo;
    }
    
    public void agregarMarca () {
        Metodos_sql metodos = new Metodos_sql();
        sentence = new String ("SELECT idmarca FROM marca");
        
        try {
            resc = metodos.consultarAlgo(sentence);
            while (resc.next()){
                jComboBox1.addItem(String.valueOf(resc.getString("idmarca")));
            }
            }catch (Exception e){
                    e.printStackTrace();
                    }
        }
    
   
    
