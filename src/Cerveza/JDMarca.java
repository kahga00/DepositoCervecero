       public Integer consulMar(){
           Metodos_sql metodos = new Metodos_sql();
        sentence = "SELECT MAX(idmarca) FROM  marca";
        try {
            resc = metodos.consultarAlgo(sentence);
            while (resc.next()) {
                ultimo = resc.getInt("MAX(idmarca)");
            }
        } catch (SQLException ex) {
            Logger.getLogger(JDExp.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("Existen " + ultimo + " marcas");
        return ultimo;
       }
       
