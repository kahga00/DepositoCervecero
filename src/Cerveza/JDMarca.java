    public void agregaFab(){
        String sentenc;
        sentenc = new String ("SELECT idfabricante FROM fabricante");
        ResultSet resc = null;
        
        try{
            resc = metodos.consultarAlgo(sentenc);
            while (resc.next()){
                jComboBox_idFab.addItem(String.valueOf(resc.getString("idfabricante")));
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
