    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
         System.out.println("Antes de: ");
        System.out.println(op.getIdExp());
        System.out.println(op.getNomExp());
        System.out.println(op.getRfc());
        System.out.println(op.getEstadoExp());
        System.out.println(op.getMunExp());
        System.out.println(op.getUbicExp());
        int e = metodos.actuExpendio(op.getIdExp(),op.getNomExp(), op.getRfc() ,op.getRespExp() , op.getEstadoExp(),op.getMunExp() , op.getUbicExp());
      
        if (e > 0) {
            JOptionPane.showMessageDialog(this, "Se actualizo expendio correctamente");
        } else {
            int i= metodos.guardarExp( op.getNomExp(), op.getRfc() ,op.getRespExp() , op.getEstadoExp(),op.getMunExp() , op.getUbicExp());
            if (i > 0) {
            JOptionPane.showMessageDialog(this, "Se ha creado expendio correctamente");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo guardar los datos");
        }
            
        }
        
       
        
        JFInicio ventana = new JFInicio();
        ventana.setVisible(true);
        this.dispose();
      
    }//GEN-LAST:event_jButton1ActionPerformed

